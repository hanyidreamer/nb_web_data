package com.elong.nb.model.rateplan;

public class HourlyRoomInfo {

	private String EarliestArrivalTime;
    private String LastestArrivalTime;

    private String MaxStayTime;

	public String getEarliestArrivalTime() {
		return EarliestArrivalTime;
	}

	public void setEarliestArrivalTime(String earliestArrivalTime) {
		EarliestArrivalTime = earliestArrivalTime;
	}

	public String getLastestArrivalTime() {
		return LastestArrivalTime;
	}

	public void setLastestArrivalTime(String lastestArrivalTime) {
		LastestArrivalTime = lastestArrivalTime;
	}

	public String getMaxStayTime() {
		return MaxStayTime;
	}

	public void setMaxStayTime(String maxStayTime) {
		MaxStayTime = maxStayTime;
	}
    
    
    
}
